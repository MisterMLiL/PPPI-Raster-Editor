// GraphicsTabletSupport.h

/*!
	\brief Модуль: Поддержка графических планшетов и стилусов

	\details В данном модуле содержатся функции для настройки графических планшетов и использования стилусов.
*/
class GraphicsTabletSupport {
public:
    /*!
		\brief Метод для настройки графического планшета

		\details Этот метод позволяет пользователю настроить параметры графического планшета.
	*/
    void configureTablet();

    /*!
		\brief Метод для использования стилуса

		\details Этот метод позволяет пользователю использовать стилус для рисования и управления.
	*/
    void useStylus();
};
