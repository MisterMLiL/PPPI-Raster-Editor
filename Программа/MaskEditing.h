// MaskEditing.h

/*!
	\brief Модуль: Работа с масками

	\details В данном модуле содержатся функции для создания и редактирования масок.
*/
class MaskEditing {
public:
    /*!
		\brief Метод для создания маски

		\details Этот метод создает новую маску для выделения части изображения.
	*/
    void createMask();

    /*!
		\brief Метод для редактирования маски

		\details Этот метод позволяет редактировать маску для выделения части изображения.
	*/
    void editMask();
};
